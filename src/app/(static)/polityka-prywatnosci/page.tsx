function renderOrderedList(lines: string[], startIndex: number, nested: boolean) {
  const orderedItemRegex = /^(\d+)\.\s+(.*)$/;

  let i = startIndex;
  const items: React.ReactNode[] = [];

  const skipEmpty = () => {
    while (i < lines.length && lines[i].trim().length === 0) i += 1;
  };

  const wrapperClassName = nested
    ? "mt-3 space-y-2 pl-5"
    : "space-y-3 pl-5";

  while (i < lines.length) {
    skipEmpty();
    if (i >= lines.length) break;

    const line = lines[i].trim();
    const match = orderedItemRegex.exec(line);
    if (!match) break;

    const [, _number, itemTextRaw] = match;
    const itemText = itemTextRaw.trim();

    // If this item looks like an introduction (ends with ":"), try to nest the next ordered list.
    const looksLikeListIntroduction = itemText.endsWith(":");
    let nestedList: React.ReactNode | null = null;

    if (looksLikeListIntroduction) {
      const nestedStart = i + 1;
      let k = nestedStart;
      while (k < lines.length && lines[k].trim().length === 0) k += 1;

      const nextLine = k < lines.length ? lines[k].trim() : "";
      const nextMatch = orderedItemRegex.test(nextLine);

      if (nextMatch) {
        const nestedParsed = renderOrderedList(lines, nestedStart, true);
        nestedList = nestedParsed.node;
        i = nestedParsed.nextIndex;
      } else {
        i += 1;
      }
    } else {
      i += 1;
    }

    items.push(
      <li key={`${nested ? "n" : "m"}-${i}-${itemText.slice(0, 20)}`} className="list-decimal">
        {looksLikeListIntroduction ? itemText.slice(0, -1) : itemText}
        {nestedList}
      </li>,
    );
  }

  const node = <ol className={wrapperClassName}>{items}</ol>;
  return { node, nextIndex: i };
}

function renderPolicyBody(body: string) {
  const lines = body.split("\n");
  const orderedItemRegex = /^(\d+)\.\s+/;

  // Split by blank lines, but render each ordered block as an <ol>.
  const blocks: React.ReactNode[] = [];
  let i = 0;

  while (i < lines.length) {
    // skip blanks
    while (i < lines.length && lines[i].trim().length === 0) i += 1;
    if (i >= lines.length) break;

    if (orderedItemRegex.test(lines[i].trim())) {
      const parsed = renderOrderedList(lines, i, false);
      blocks.push(parsed.node);
      i = parsed.nextIndex;
      continue;
    }

    // Fallback: paragraph line (should be rare with current input).
    const paragraphLines: string[] = [];
    while (i < lines.length && lines[i].trim().length > 0 && !orderedItemRegex.test(lines[i].trim())) {
      paragraphLines.push(lines[i].trim());
      i += 1;
    }
    blocks.push(<p key={`p-${i}`} className="leading-7">{paragraphLines.join(" ")}</p>);
  }

  return <div className="space-y-3">{blocks}</div>;
}

